1- present simple
(A)-regular habits and repeated actions.
(B)-permanent situations.
(C)-give instructions and directions.
(D)-tell stories and talk about films ,books and games.
(E)-facts and general accepted trust.
------------------------------------------------------------------------------------------------------------------------
2- present continuous
(A)-actions at the moment of speaking.
(B)-temporary situations
(C)-actions done more than expected.(complaint)&(envy)
(D)-change situations
------------------------------------------------------------------------------------------------------------------------
3-present perfect simple
(A)-to talk about  a time period (actions) that is not finished
(B)-to show that something happened at some point in the past before now but we don't know time of action.
(C)-to talk about a present situation which started in the past, usually with (for/since)
    I've worked really hard for the last two weeks.
(D)-to talk about something that happened at an unstated time in the past but is connected to the present:
حدث حصل في وقت لسا منتهاش -حدث حصل في الماضي مكمل في الحاضر -حدث حصل في الماضي بس مش معروف وقتة
------------------------------------------------------------------------------------------------------------------------
4-present perfect continuous
(A)-We can use either the present perfect simple or the present perfect continuous to say how long a situation or
activity has been going on (often with/For or since)
I've felt tired for weeks.
I've been feeling tired since I started this course.
I've worked at the restaurant since I moved here.
I've been working at the restaurant for three years.
حدث ابتدي في الماضي مستمر في الحاضر ومكمل في المستقبل
------------------------------------------------------------------------------------------------------------------------
Present perfect continuous
• emphasises how long:
I've been reading for the past two weeks.
• focuses on the activity itself (it does not show whether the activity is completed or not):
I've been writing my essay. (we don't know if the essay is finished or not)
What have you been doing? (the boy's mother is interested in the activity that made him so dirty now)
Present perfect simple
• says how many times:
I've read three articles.
Present perfect
+. focuses on the result or completion of the activity:
I've written my essay. (the essay is finished but we don't know when)
